/** Maps a contact link's icon key to its component. */
export function ContactIcon({ type, ...props }: { type: ContactLink['icon'] } & IconProps) {
  switch (type) {
    case 'github':
      return <GitHubIcon {...props} />;
    case 'mail':
      return <MailIcon {...props} />;
    case 'linkedin':
      return <LinkedInIcon {...props} />;
    case 'phone':
      return <PhoneIcon {...props} />;
    default:
      return <ExternalIcon {...props} />;
  }
}
